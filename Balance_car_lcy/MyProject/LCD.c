{
     LCD_SET_REGION(x,y,x,y);
	  
	   LCD_WR_DATA_16b(colour);
}


/**
* @brief LCD Display a character
*/
void LCD_DISP_CHAR(uint16_t x, uint16_t y, char dat)
{
     int8_t i,j;
	   uint8_t temp;
	   
	   for(i=0; i<16; i++)
	   {
			   LCD_SET_REGION(x, y+i, x+7, y+i);
			   temp = LCD_ASCII[dat-32][15-i];
	       for(j=7; j>=0; j--)
         {
				     if(temp & (1<<j))
						    LCD_WR_DATA_16b(LCD_TXC);
						 else
							  LCD_WR_DATA_16b(LCD_BGC);
				 }			 
		 }
}


/**
* @brief LCD Display a string
*/
