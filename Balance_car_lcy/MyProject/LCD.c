/**
* @Brief: Set the display region of LCD
*/
void LCD_SET_REGION(uint16_t x1, uint16_t y1, uint16_t x2, uint16_t y2)
{
     LCD_WR_COMMAND(0x2A);      //Column Address Set
	   LCD_WR_DATA_16b(x1+52);
	   LCD_WR_DATA_16b(x2+52);
	 
	   LCD_WR_COMMAND(0x2B);      //Row Address Set
	   LCD_WR_DATA_16b(y1+40);
	   LCD_WR_DATA_16b(y2+40);
	
	   LCD_WR_COMMAND(0x2C);      //Memory Write
}

/**
* @Brief: FILL the LCD with one colour
*/
void LCD_FILL(uint16_t colour)
{
     uint16_t i,j;
	
	   LCD_SET_REGION(0, 0, LCD_Hight-1, LCD_Width-1);
	
	   for(i=0; i<LCD_Width; i++)
	   {
		     for(j=0; j<LCD_Hight; j++)
			   {
				     LCD_WR_DATA_16b(colour);
				 }
		 }
}


